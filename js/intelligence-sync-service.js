/**
 * 留情局情报同步服务
 * 从 Supabase 获取 AI 采集的情报数据
 */

const SYNC_INTELLIGENCE_KEY = 'liuqingju_intelligence';
const SYNC_META_KEY = 'liuqingju_intelligence_meta';
const SYNC_INTERVAL = 30 * 60 * 1000; // 30 分钟

class IntelligenceSyncService {
    constructor(supabaseUrl, supabaseKey) {
        this.supabaseUrl = supabaseUrl;
        this.supabaseKey = supabaseKey;
        this.syncTimer = null;
    }

    async fetchIntelligence(limit = 50) {
        if (!this.supabaseUrl || !this.supabaseKey) {
            console.warn('Supabase 未配置，使用本地数据');
            return this.getLocalIntelligence();
        }

        try {
            const url = `${this.supabaseUrl}/rest/v1/intelligence_posts?select=*&order=published_at.desc&limit=${limit}`;
            const response = await fetch(url, {
                method: 'GET',
                headers: {
                    'apikey': this.supabaseKey,
                    'Authorization': `Bearer ${this.supabaseKey}`,
                    'Content-Type': 'application/json'
                }
            });

            if (!response.ok) {
                throw new Error(`HTTP ${response.status}`);
            }

            const data = await response.json();
            // 生产环境不应记录完整数据
            console.log(`从 Supabase 获取 ${data.length} 条数据`);

            const converted = data.map(post => this.convertToLocalFormat(post));

            return {
                success: true,
                data: converted,
                count: converted.length,
                timestamp: new Date().toISOString()
            };

        } catch (error) {
            console.error('获取情报失败:', error);
            return {
                success: false,
                error: error.message,
                data: this.getLocalIntelligence()
            };
        }
    }

    convertToLocalFormat(post) {
        return {
            id: post.id || `intel_${Date.now()}`,
            title: post.title,
            tag: (post.tag || 'admission').toLowerCase(),
            tagLabel: (post.tag_label || 'ADMISSION').toUpperCase(),
            time: post.published_at || post.created_at,
            views: 0,
            content: post.content,
            source: post.university || post.source_university || 'AI采集',
            sourceUrl: post.source_url,
            isAutoGenerated: post.is_auto_generated || true,
            degreeLevel: post.degree_level,
            confidence: post.confidence || 0.8
        };
    }

    getLocalIntelligence() {
        try {
            const stored = localStorage.getItem(SYNC_INTELLIGENCE_KEY);
            return stored ? JSON.parse(stored) : [];
        } catch (error) {
            console.error('读取本地情报失败:', error);
            return [];
        }
    }

    async syncToLocal(force = false) {
        const meta = this.getSyncMeta();

        if (!force && meta.lastSync) {
            const timeSinceLastSync = Date.now() - new Date(meta.lastSync).getTime();
            if (timeSinceLastSync < SYNC_INTERVAL) {
                console.log(`同步间隔未到，跳过 (${Math.round((SYNC_INTERVAL - timeSinceLastSync) / 1000 / 60)}分钟后重试)`);
                return { status: 'skipped', reason: 'sync_interval' };
            }
        }

        const result = await this.fetchIntelligence();

        if (result.success) {
            if (result.data.length > 0) {
                localStorage.setItem(SYNC_INTELLIGENCE_KEY, JSON.stringify(result.data));
                this.updateSyncMeta({
                    lastSync: new Date().toISOString(),
                    syncedCount: result.data.length,
                    newCount: result.data.length
                });
                console.log(`同步完成：更新 ${result.data.length} 条情报`);
                return { status: 'success', added: result.data.length, total: result.data.length };
            } else {
                this.updateSyncMeta({ lastSync: new Date().toISOString() });
                return { status: 'success', added: 0, total: 0 };
            }
        }

        return { status: 'error', error: result.error };
    }

    getSyncMeta() {
        try {
            const stored = localStorage.getItem(SYNC_META_KEY);
            return stored ? JSON.parse(stored) : {
                lastSync: null,
                syncedCount: 0,
                newCount: 0
            };
        } catch (error) {
            return { lastSync: null, syncedCount: 0, newCount: 0 };
        }
    }

    updateSyncMeta(updates) {
        const current = this.getSyncMeta();
        const updated = { ...current, ...updates };
        localStorage.setItem(SYNC_META_KEY, JSON.stringify(updated));
    }

    startAutoSync(intervalMs = SYNC_INTERVAL) {
        this.stopAutoSync();
        this.syncTimer = setInterval(() => {
            this.syncToLocal();
        }, intervalMs);
        console.log(`情报自动同步已启动 (间隔: ${intervalMs / 1000 / 60} 分钟)`);
    }

    stopAutoSync() {
        if (this.syncTimer) {
            clearInterval(this.syncTimer);
            this.syncTimer = null;
            console.log('情报自动同步已停止');
        }
    }

    async manualSync() {
        return await this.syncToLocal(true);
    }

    clearIntelligence() {
        localStorage.removeItem(SYNC_INTELLIGENCE_KEY);
        localStorage.removeItem(SYNC_META_KEY);
    }

    getStats() {
        const data = this.getLocalIntelligence();
        return {
            total: data.length,
            byTag: data.reduce((acc, item) => {
                acc[item.tag] = (acc[item.tag] || 0) + 1;
                return acc;
            }, {}),
            recent: data.filter(item => {
                const date = new Date(item.time);
                const now = new Date();
                const diff = now - date;
                return diff < 7 * 24 * 60 * 60 * 1000;
            }).length
        };
    }
}